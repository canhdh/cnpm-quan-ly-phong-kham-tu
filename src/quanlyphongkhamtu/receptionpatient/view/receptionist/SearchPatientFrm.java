    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        JButton btnClicked = (JButton)evt.getSource();
        if (btnClicked.equals(btnSearch)) {
            if ((txtKey.getText() == null) || (txtKey.getText().length() == 0))
                return;
            PatientDAO patientDAO = new PatientDAO();
            listPatient = new ArrayList<>(patientDAO.searchPatient(txtKey.getText().trim()));
            
            if (listPatient == null || listPatient.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy bệnh nhân!",
                    "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                String[] columnNames = {"STT", "ID", "Tên bệnh nhân", "Tuổi", "Địa chỉ", "CCCD", "SĐT"};
                String[][] values = new String[listPatient.size()][7];
                for (int i = 0; i < listPatient.size(); i++) {
                    values[i][0] = String.valueOf(i + 1);
                    values[i][1] = String.valueOf(listPatient.get(i).getId());
                    values[i][2] = listPatient.get(i).getFullName();
                    values[i][3] = String.valueOf(listPatient.get(i).getAge());
                    values[i][4] = listPatient.get(i).getAddress();
                    values[i][5] = listPatient.get(i).getCitizenId();
                    values[i][6] = listPatient.get(i).getPhone();
                }
                DefaultTableModel tableModel = new DefaultTableModel(values, columnNames) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        // make cell be unable to edit
                        return false;
                    }
                };
                tblListPatient.setModel(tableModel);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

