    private void tblListPatientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListPatientMouseClicked
        // TODO add your handling code here:
        // get the colume of the button
        int colume = tblListPatient.getColumnModel().getColumnIndexAtX(evt.getX());
        // get the row of the button
        int row = evt.getY() / tblListPatient.getRowHeight();
        
        // Checking the row or column is valid or not
        if (row < tblListPatient.getRowCount() && row >= 0
                && colume < tblListPatient.getColumnCount() && colume >= 0) {
            (new SearchActiveRoomFrm(user, listPatient.get(row))).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_tblListPatientMouseClicked

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        if ((evt.getSource() instanceof JButton) && (((JButton) evt.getSource()).equals(btnBack))) {
            (new PatientReceptionFrm(user)).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnBackActionPerformed

